def full_day_to_int(day_name):
    if day_name == "Monday":
        return 0
    elif day_name == "Tuesday":
        return 1
    if day_name == "Wednesday":
        return 2
    elif day_name == "Thursday":
        return 3
    if day_name == "Friday":
        return 4
    elif day_name == "Saturday":
        return 5
    elif day_name == "Sunday":
        return 6
